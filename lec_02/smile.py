import turtle as trt
trt.shape('turtle')
trt.penup()
trt.goto(100,0)
trt.pendown()
trt.left(90)
trt.fillcolor('yellow')
trt.begin_fill()
trt.circle(100)
trt.end_fill()
trt.penup()
trt.goto(-30,50)
trt.pendown()
trt.fillcolor('blue')
trt.begin_fill()
trt.circle(20)
trt.end_fill()
trt.penup()
trt.goto(60,50)
trt.pendown()
trt.fillcolor('blue')
trt.begin_fill()
trt.circle(20)
trt.end_fill()
trt.penup()
trt.goto(0,30)
trt.pendown()
trt.width(7)
trt.left(180)
trt.forward(50)
trt.penup()
trt.goto(70,-10)
trt.color('red')
trt.pendown()
trt.circle(-70,180,30)

